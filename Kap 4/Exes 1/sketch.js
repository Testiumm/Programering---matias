// =
console.log(2 == 2);
console.log(2 == "2");
/*
Jeg tror den viser true fordi de har samme værdi 
og fordi der kun er 2 "=" betyder det at det ingen forskel gør at de har forskellige type
*/

// !=
console.log(3 != 2);
console.log("hello" != "Hello");
/*
Jeg tror begge viser true da de er forskellige fra hindanen
*/
//===
console.log(2 === 2);
console.log(2 === "2");
/*
Den første vil vise true da de har samme værdi men og samme type
Men den anden vil ikke vise true da de ikke har samen type her men udenlukkende samme værdi
*/

// !==
console.log(2 !== "2");
console.log(2 !== 2);
/*
Den er true fordi hvis det var "===" havde det været false
Den anden er false fordi de ens på alle punkter
*/
